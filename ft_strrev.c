#include "libft.h"

char		*ft_strrev(char *str)
{
	int		i;
	int		j;
	int		tmp;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	while (j < i + 1 / 2)
	{
		tmp = str[j];
		str[j] = str[i];
		str[i] = tmp;
		i--;
		j++;
	}
	return (str);
}
